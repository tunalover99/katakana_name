// 평음 -> 탁음(유성음) 매핑: 비음 받침(ㄴ,ㅁ,ㅇ) 뒤에서 ㄱ,ㄷ,ㅂ,ㅈ 초성이 유성음화됨
const VOICING_MAP = {
  'キャ':'ギャ','キュ':'ギュ','キョ':'ギョ','キェ':'ギェ',
  'カ':'ガ','キ':'ギ','ク':'グ','ケ':'ゲ','コ':'ゴ',
  'ピャ':'ビャ','ピュ':'ビュ','ピョ':'ビョ','ピェ':'ビェ',
  'パ':'バ','ピ':'ビ','プ':'ブ','ペ':'ベ','ポ':'ボ',
  'テャ':'デャ','テョ':'デョ','テュ':'デュ',
  'タ':'ダ','チ':'ヂ','ツ':'ヅ','テ':'デ','ト':'ド',
  'チャ':'ジャ','チョ':'ジョ','チュ':'ジュ','チェ':'ジェ',
};
const VOICING_KEYS = Object.keys(VOICING_MAP).sort((a, b) => b.length - a.length);

function decomposeHangul(char) {
  const code = char.charCodeAt(0) - 0xAC00;
  if (code < 0 || code > 11171) return null;
  const cho = Math.floor(code / 588);
  const jung = Math.floor((code % 588) / 28);
  const jong = code % 28;
  return { cho: CHO[cho], jung: JUNG[jung], jong: JONG[jong] };
}

function jongToKatakana(jong) {
  if (!jong) return '';
  if (jong === 'ㄴ') return 'ン';
  if (jong === 'ㅇ') return 'ン';
  if (jong === 'ㄹ') return 'ル';
  if (jong === 'ㅁ') return 'ム';
  if (['ㄱ', 'ㄲ', 'ㄳ', 'ㄺ'].includes(jong)) return 'ク';
  if (['ㅂ', 'ㄼ', 'ㄿ'].includes(jong)) return 'プ';
  if (['ㄷ', 'ㅅ', 'ㅆ', 'ㅈ', 'ㅊ', 'ㅌ', 'ㄵ', 'ㄶ', 'ㄽ', 'ㄾ', 'ㅀ', 'ㅄ', 'ㅎ'].includes(jong)) return 'ッ';
  return '';
}

function voiceIfNeeded(katakana, prevJong) {
  // 비음 받침(ㄴ,ㅁ,ㅇ)과 유음 받침(ㄹ) 뒤에서 평음 초성이 유성음화되는 경향 반영
  if (!['ㄴ', 'ㅁ', 'ㅇ', 'ㄹ'].includes(prevJong)) return katakana;
  for (const key of VOICING_KEYS) {
    if (katakana.startsWith(key)) {
      return VOICING_MAP[key] + katakana.slice(key.length);
    }
  }
  return katakana;
}

function convertSyllable(char, prevJong) {
  const d = decomposeHangul(char);
  if (!d) return { text: char, jong: null };

  const key = d.cho + d.jung;
  let result = SYLLABLE_MAP[key] || '';

  if (['ㄱ', 'ㄷ', 'ㅂ', 'ㅈ'].includes(d.cho)) {
    result = voiceIfNeeded(result, prevJong);
  }

  if (d.jong) {
    result += jongToKatakana(d.jong);
  }

  return { text: result, jong: d.jong || null };
}

/**
 * 한글 텍스트를 가타카나로 변환합니다.
 * 한글이 아닌 문자(공백, 숫자, 영문, 콤마 등)는 그대로 유지됩니다.
 */
function convertHangulToKatakana(text) {
  if (!text) return '';
  let output = '';
  let prevJong = null;

  for (const char of text) {
    if (decomposeHangul(char)) {
      const { text: t, jong } = convertSyllable(char, prevJong);
      output += t;
      prevJong = jong;
    } else {
      output += char;
      prevJong = null; // 한글이 아닌 문자가 끼면 유성음화 연쇄는 끊김
    }
  }
  return output;
}
